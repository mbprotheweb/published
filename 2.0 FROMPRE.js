// === Auto Answer Script with Console Logging (debounced last-question safe) ===
const LOG_PREFIX = '[AutoAnswer]';
const log = (...args) => console.log(LOG_PREFIX, ...args);
const warn = (...args) => console.warn(LOG_PREFIX, ...args);

// Track which question IDs we've already answered to avoid double-click unchecking
window.__answeredIds = window.__answeredIds || new Set();

function getCourseTitle() {
  const titleEl = document.querySelector('h1.js-header-text');
  const t = titleEl?.innerText.trim();
  log('Course title:', t);
  return t;
}

function getQuestionContainer() {
  return document.querySelector('.question-content legend.multiple-answer-question');
}

function getQuestionId() {
  const legend = getQuestionContainer();
  const id = legend?.id || null;
  if (id) return id;
  // Fallback to hashed stem portion to have a stable-ish ID
  const stem = getQuestionText();
  return 'stem:' + normalize(stem).slice(0, 100);
}

function getQuestionText() {
  const editor = document.querySelector('.question-content legend .ql-editor');
  const raw = editor ? editor.innerText : '';
  if (!raw) return '';
  const t = raw.replace(/\s+/g, ' ').trim();
  const qMarkIdx = t.lastIndexOf('?');
  if (qMarkIdx !== -1) {
    const pre = t.slice(0, qMarkIdx + 1);
    let start = Math.max(pre.lastIndexOf('. '), pre.lastIndexOf('! '), pre.lastIndexOf('? '), pre.lastIndexOf('\n'));
    if (start < 0) start = 0;
    const stem = pre.slice(start).trim();
    log('Detected question stem:', stem);
    return stem;
  }
  const tail = t.slice(Math.max(0, t.length - 200)).trim();
  log('Fallback question tail:', tail);
  return tail;
}

function getAnswerOptions() {
  const opts = Array.from(document.querySelectorAll('.option-answer'));
  log('Found answer options:', opts.length);
  return opts;
}

function normalize(text) {
  if (!text) return '';
  const ascii = text
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[]/g, '"')
    .replace(/[]/g, "'")
    .replace(/[^\w\s\?]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase();
  return ascii;
}

function findCorrectAnswerElement(correctText) {
  const target = normalize((correctText || '').replace(/^[A-D]\s/, ''));
  log('Target answer (normalized):', target);
  return getAnswerOptions().find(option => {
    const textEl = option.querySelector('.ql-editor');
    const opt = normalize(textEl?.innerText || '');
    const ok = opt === target || opt.includes(target) || target.includes(opt);
    if (ok) log('Matched option text:', textEl?.innerText?.trim());
    return ok;
  });
}

function ensureAnsweredOnce() {
  const qid = getQuestionId();
  if (window.__answeredIds.has(qid)) {
    log('Question already answered (by ID):', qid);
    return true;
  }

  const stem = getQuestionText();
  const courseTitle = getCourseTitle();
  if (!courseTitle || !questions[courseTitle]) {
    warn('Course title not found or no matching question list');
    return false;
  }
  const stemN = normalize(stem);
  const bank = questions[courseTitle];
  const match = bank.find(q => {
    const qn = normalize(q["Full Question Text"]);
    return (
      stemN === qn ||
      stemN.endsWith(qn) ||
      qn.endsWith(stemN) ||
      stemN.includes(qn) ||
      qn.includes(stemN)
    );
  });
  if (!match) {
    warn('Question not found in JSON for course:', courseTitle, '\nStem seen:', stem);
    return false;
  }
  log('Matched JSON question:', match["Full Question Text"]);
  log('Expected answer:', match["Correct Answer"]);

  const answerEl = findCorrectAnswerElement(match["Correct Answer"]);
  if (!answerEl) {
    warn('Correct answer not found in DOM for matched question');
    return false;
  }
  const input = answerEl.querySelector('input');
  if (!input) {
    warn('No input found inside matched answer element');
    return false;
  }

  // If already checked, don't click again (avoid unchecking on last question)
  if (input.checked) {
    log('Correct answer already selected; skipping click.');
    window.__answeredIds.add(qid);
    return true;
  }

  // Prefer clicking the LABEL to mimic user action; fallback to input
  const label = answerEl.querySelector('label');
  if (label) {
    label.click();
    log('Clicked correct answer label.');
  } else {
    input.click();
    log('Clicked correct answer input.');
  }

  // Mark answered after a short delay to allow Angular to update
  setTimeout(() => {
    if (input.checked) {
      window.__answeredIds.add(qid);
      log('Answer registered as checked. Marked question answered:', qid);
    } else {
      warn('Answer did not register as checked yet.');
    }
  }, 200);

  return true;
}

